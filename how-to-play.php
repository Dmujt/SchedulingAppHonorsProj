    <div  class="howto">
        <p>
            When you are ready to play a game, go to the View/Create games page. There you will find a list of
            games waiting for a second player that you can join, or you can create your own game. Once two players
            have joined a game you will be taken to the game play screen if it is your turn or the waiting screen
            if it is not.
        </p>
        <p>
            On your turn, select a pipe from your array and click on a leak to place it on your pipeline.
            Invalid moves such as placing a pipe on the other player's pipeline or with an opening to the edge of
            the game board are not allowed. To rotate a pipe select the radio button to the right of the pipe and
            press rotate until the piece is in the desired orientation. Once a piece has been placed on the game
            board a new random piece will take it's place in your array of pipes. If you would like to get a new
            random piece you can discard a selected piece by pressing the Discard button. Doing so will end
            your turn.
        </p>
        <p>
            If you have a completed and sealed pipeline press the Open Valve button to win the game, but beware,
            if you press the Open Valve button when your pipeline has a leak you will lose the game!
            If you know that all is lost you can press the Give Up button to concede. Once the game is finished
            you can return to the games page to create or join a new game. Happy Steampunking!
        </p>

    </div>

